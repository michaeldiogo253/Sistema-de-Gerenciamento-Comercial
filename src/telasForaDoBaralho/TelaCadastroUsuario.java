    public void listar() {
        UsuarioDAO dao = new UsuarioDAO();
        List<Usuario> lista = dao.listarUsuario();
        DefaultTableModel dados = (DefaultTableModel) TabelaUsuario.getModel();
        dados.setNumRows(0); // limpa os dados 

        for (Usuario c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getTipoConta(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                c.getCargo(),
                c.getCelular(),
                c.getUsuarioLogin(),
                c.getSenhaLogin()
            });
        }
    }

